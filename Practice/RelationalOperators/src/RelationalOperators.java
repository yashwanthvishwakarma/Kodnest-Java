
public class RelationalOperators {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c = 30;
		// "==" is used to check weather the two variables values are equal
		System.out.println(a == c);
		
		//"!=" is used to check weather the two variables values are not equal 
		System.out.println(a != b);
		
		//">" is use to check if the value is greater than the other or not
		System.out.println(a > b);
		
		//"<" is used to check the value is lesser than other or not 
		System.out.println(a < b);
		
		//">=" is used to check the value is greater than or equal to the other value
		System.out.println(a >= b);
		
		//"<=" is used to check the value is lesser than or equal to the other value
		System.out.println(a <= b);
		
		//">=" is used to check the value is greater than or equal to the other value
		System.out.println(b >= a);
		
		//"<=" is used to check the value is lesser than or equal to the other value
		System.out.println(a >= c);
		
		//These statements will give output in boolean type which is "true" or "false"

	}

}
