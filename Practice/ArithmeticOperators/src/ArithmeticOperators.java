
public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 10;
		
		// adding two numbers using "+" operator
		int add = a + b;
		
		// subtracting two numbers using "-" operator
		int sub = a - b;
		
		// multiplying two numbers using "*" operator
		int mul = a * b;
		
		// dividing two numbers using "/" operator
		int div = a / b;
		
		// getting the remainder of the two numbers using "%" operator which is also called as "Modulus"
		int mod = a % b;
		
		//Putting everything is single statement and displaying it using print statement 
		System.out.println("The results are : "+add+" "+sub+" "+mul+" "+div+" "+mod);
	}

}
